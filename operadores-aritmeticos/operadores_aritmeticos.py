produtos_1 =20
produtos_2 = 10

# Adição, resultado = 30
print(produtos_1 + produtos_2)

# Subtração, resultado = 10
print(produtos_1 - produtos_2)

# Divisão, resultado = 2.0
print(produtos_1 / produtos_2)

# Divisão inteira, resultado = 2
print(produtos_1 // produtos_2)

# Multiplicação, resultado = 200
print(produtos_1 * produtos_2)

# Módulo, resultado = 0
print(produtos_1 % produtos_2)

# Exponenciação, resultado = 10240000000000
print(produtos_1 ** produtos_2)

