# Atribuição Simples
saldo = 500
print(saldo) 
saldo = 200
print(saldo)  

# Atribuição com Adição
saldo += 10
print(saldo) # Resultado: 210 

# Atribuição com Subtração
saldo -= 5
print(saldo) # Resultado: 205

# Atribuição com Divisão Inteira
saldo //= 2
print(saldo) # Resultado: 102

# Atribuição com Divisão
saldo /= 2
print(saldo) # Resultado: 51.0

# Atribuição com Multiplicação
saldo *= 10
print(saldo) # Resultado: 510.0

# Atribuição com Modularização
saldo %= 4
print(saldo) # Resultado: 2.0

# Atribuição com Exponenciação
saldo **= 2
print(saldo) # Resultado: 4.0